function UpdateCost() {
  var sum = 0;
  var gn, elem;
  for (i=0; i<31; i++) {
    gn = 'id'+i;
    elem = document.getElementById(gn);
    if (elem.checked == true) { sum += Number(elem.value); }
  }
  document.getElementById('totalcost').value = sum.toFixed(2);
 
} 




