//初期処理
function onReady() {    
    var versionNum = 1;
    var oldVersion = getVersion();
    if(oldVersion < versionNum) {
     deleteData();
     addData();
    }
    saveVersion(versionNum);
     
    initTopPage();
    $("#TopListView a.show").live("tap", onShowLink);
    $("#search-word-input").change(keySearch);
    $("#child").change(keySearch);
    $("#parent").change(keySearch);
    
    monaca.splashScreen.hide();
}

//リスト作成
function initTopPage() {
    $("#TopListView").empty();
    
    var list = getDataList();
    for (var i in list) {
        var memo = list[i];
        var title =memo.title;        
        $li = $("<li><a href='#' class='show'><h1></h1><h2></h2><h3></h3><h4></h4><p></p></a></li>");
        $li.data("id", memo.id);        
        $li.find("h1").text(memo.title);
        $li.find("h2").text(memo.data1);
        $li.find("h3").text(memo.data2);
        $li.find("h4").text(memo.kana);
        $li.find("p").text(memo.bunrui1);
        $li.find("h2").hide();
        $li.find("h3").hide();
        $li.find("h4").hide();
        $li.find("p").hide();
        $("#TopListView").prepend($li);
    }
    if (list.length == 0) {
        $li = $("<li>No memo found</li>");
        $("#TopListView").prepend($li);
    }
    $("#TopListView").listview("refresh");  // Call refresh after manipulating list
    
    $('#TopListView').html(
    $('#TopListView > li').sort(function(a, b) {
		return parseInt($(a).attr('id'), 10) - parseInt($(b).attr('id'), 10);
	})
    );
    $("#TopListView").listview("refresh");
    
}

//詳細ページを開く
function onShowLink() {
    var $li = $(this).parent();
    var memoTitle = $li.find("h1").text();
    var memoHtml = $li.find("h2").html().replace(/\n/g, "<br>");
    var memoHtml2 = $li.find("h3").html().replace(/\n/g, "<br>");

    var memoKana = $li.find("h4").text();
    var memoBunrui = $li.find("p").text();
    $("#ShowPage h1").text(memoTitle);
    $("#ShowPage h2").html(memoHtml);
    $("#ShowPage h3").html(memoHtml2);
    $("#ShowPage h4").html(memoKana);
    $("#ShowPage p").html(memoBunrui);
    $.mobile.changePage("#ShowPage");
}

var timeout_id = null;

//検索
function keySearch (){
    var input = $("#search-word-input");
    if(timeout_id) clearTimeout(timeout_id);
    timeout_id = setTimeout(function(){     
    timeout_id = null;    
    
    var word_re = new RegExp(input.val(), "i");   
    if(input.val() == "名称検索（かな・漢字名称・分類名など）"){word_re= "";}
    $("#TopListView li").each(function(){         
        var li = $(this);
        if(li.text().match(word_re))
        {             
            var word_re2 = new RegExp($("#parent option:selected").text(), "i"); 
            if($("#parent option:selected").text() == "分類検索"){word_re2= "";}
            if(li.text().match(word_re2))
            { 
                var word_re3 = new RegExp($("#child option:selected").text(), "i"); 
                if($("#child option:selected").text() == "50音検索"){word_re3= "";}
                if(li.text().match(word_re3))
                { 
                   li.show();
                }
                else
                {  
                    li.hide();
                }
            }
            else{  
                li.hide();
            }
        }
        else{  
            li.hide();
        }
     });
   }, 300);
 }


