//データ取得
function getDataList() {
    var list = localStorage.getItem("data_list");
    if (list == null) {
        return new Array();
    } else {
        return JSON.parse(list);
    }
}
//データ保存
function saveDataList(list) {
    try {
        localStorage.setItem("data_list", JSON.stringify(list));
    } catch (e) {
        alert('Error saving to storage.');
        throw e;
    }
}

//バージョン取得
function getVersion() {
    var val = localStorage.getItem("version");
    if (val == null) {
        return 0;
    } else {
        return parseInt(val);
    }
}
//バージョン保存
function saveVersion(val) {
    try {
        localStorage.setItem("version", val);
    } catch (e) {
        alert('Error saving to storage.');
        throw e;
    }
}

//データ削除
function deleteData() {
     window.localStorage.clear();
}





